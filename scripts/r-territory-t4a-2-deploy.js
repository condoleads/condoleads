let success = 0;
let skipped = 0;
const errors = [];

for (const f of FILES) {
  const target = path.resolve(f.target);
  const dir = path.dirname(target);

  if (fs.existsSync(target)) {
    skipped++;
    console.log('SKIP (exists): ' + f.target);
    continue;
  }

  try {
    fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(target, f.content, { encoding: 'utf8', flag: 'wx' });
    const size = fs.statSync(target).size;
    console.log('OK: ' + f.target + ' (' + size + ' bytes)');
    success++;
  } catch (e) {
    errors.push({ file: f.target, error: e.message });
    console.error('FAIL: ' + f.target + ' -- ' + e.message);
  }
}

console.log('');
console.log('Summary: ' + success + ' created, ' + skipped + ' skipped, ' + errors.length + ' failed');
if (errors.length > 0) process.exit(1);
